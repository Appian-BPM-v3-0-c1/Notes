public class Triangle {

    private double height;
    private double base;

    public Triangle() {
        super();
    }

    public double getHeight() {
        return height;
    }

    public double getBase() {
        return base;
    }

    public void setHHeight(double height) {
        this.height = height;
    }

    public void setBase(double width) {
        this.base = width;
    }




}
